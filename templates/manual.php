                <p><b>2. Порядок списания и передачи показаний индивидуальных приборов учета</b></p>
                <p>Показания приборов учета рекомендуем списывать в период с 23-го по 25-е число текущего месяца. Чтобы оплачивать фактическое потребление коммунальных услуг в вашем жилом помещении, передавайте показания счетчиков до 10 числа месяца, следующего за расчетным.</p>
