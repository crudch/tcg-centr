                <p><b>3. Расчет размера платы исходя из среднемесячного объема потребления</b></p>
                <p>Проводится в случае:</p>
                <ul>
                    <li>Если ваш индивидуальный прибор учета вышел из строя, либо закончился срок его эксплуатации;</li>
                    <li>Если вы не предоставили показания приборов учета до 10 числа месяца, следующего за расчетным.</li>
                </ul>
                <p>Порядок расчета:</p>
                <ul>
                    <li>Среднемесячный объем потребления коммунальных услуг определяется по показаниям индивидуальных приборов учета за период не менее 6 месяцев (по услуге «отопление» - за отопительный период).</li>
                    <li>Если индивидуальный прибор учета действовал менее 6 месяцев, среднемесячный объем определяется исходя из реального срока действия счетчика, но не менее 3 месяцев (для отопления – не менее 3 месяцев отопительного периода).</li>
                    <li>Если вы не предоставляете показания приборов учета, начисления по среднемесячному объему потребления проводятся не более 6 расчетных периодов подряд. Затем - по нормативам потребления.</li>
                    <li>Если прибор учета вышел из строя, либо закончился срок его эксплуатации, начисления по среднемесячному объему потребления проводятся не более 3 расчетных периодов подряд. Затем – по нормативам потребления.</li>
